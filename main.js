for (let i = 0; i <= 10; i++) {
  console.log(i);
}

console.log("For druhý příklad - rozdíl mezi <= a <");
for (let i = 0; i < 10; i++) {
  console.log(i);
}


console.log("For třetí příklad - sestupná řada 10 - 0");
